const SEO_PAGE_DEFAULT_KEY = {
    "index.html": "home",
    "": "home",
    "nha-dat-ban.html": "sale",
    "nha-dat-cho-thue.html": "rent",
    "du-an.html": "project",
    "gia-nha-dat.html": "price",
    "tin-tuc.html": "news",
    "xay-dung.html": "construction",
    "danh-ba-doanh-nghiep.html": "directory",
    "danh-ba-moi-gioi.html": "directory"
};

function getSeoContentByKey(pageKey) {
    return SEO_CONTENT[pageKey] || "";
}

function getSeoCategoryContent(categorySlug) {
    return SEO_CATEGORY_CONTENT[categorySlug] || "";
}

function getCurrentPageName() {
    const path = window.location.pathname || "";
    const parts = path.split("/");
    const fileName = parts[parts.length - 1] || "";

    if (!fileName && (path.endsWith("/") || path === "")) {
        return "index.html";
    }

    return fileName || "index.html";
}

function getCategoryFromUrl() {
    const params = new URLSearchParams(window.location.search);
    return (params.get("category") || "").trim();
}

function resolveSeoKeyByCurrentPage() {
    const pageName = getCurrentPageName().toLowerCase();
    return SEO_PAGE_DEFAULT_KEY[pageName] || "home";
}

function resolveSeoContent(pageKey) {
    const categorySlug = getCategoryFromUrl();

    if (categorySlug) {
        const categoryContent = getSeoCategoryContent(categorySlug);
        if (categoryContent) {
            return categoryContent;
        }
    }

    return getSeoContentByKey(pageKey);
}

function buildSeoHtml(containerId, content) {
    return `
        <div class="seo-wrapper seo-block">
            <div class="seo-text collapsed" id="${containerId}-text">
                ${content}
            </div>

            <button
                type="button"
                class="seo-toggle-btn"
                id="${containerId}-toggle"
                aria-expanded="false"
            >
                <span class="seo-toggle-label">Xem thêm</span>
                <i class="fa-solid fa-chevron-down seo-toggle-icon"></i>
            </button>
        </div>
    `;
}

function renderSeoContent(containerId = "seoContent", pageKey = "") {
    const container = document.getElementById(containerId);
    if (!container) return;

    const finalPageKey = pageKey || resolveSeoKeyByCurrentPage();
    const content = resolveSeoContent(finalPageKey);

    if (!content) {
        container.innerHTML = "";
        return;
    }

    container.innerHTML = buildSeoHtml(containerId, content);

    const toggleBtn = document.getElementById(`${containerId}-toggle`);
    const seoText = document.getElementById(`${containerId}-text`);
    const toggleLabel = toggleBtn ? toggleBtn.querySelector(".seo-toggle-label") : null;

    if (!toggleBtn || !seoText || !toggleLabel) return;

    requestAnimationFrame(() => {
        const wasCollapsed = seoText.classList.contains("collapsed");
        const collapsedHeight = wasCollapsed ? seoText.clientHeight : 0;
        const fullHeight = seoText.scrollHeight;

        if (fullHeight <= collapsedHeight + 8 || fullHeight <= 90) {
            toggleBtn.style.display = "none";
            seoText.classList.remove("collapsed");
            return;
        }

        toggleBtn.style.display = "inline-flex";
    });

    toggleBtn.addEventListener("click", () => {
        const isCollapsed = seoText.classList.contains("collapsed");

        if (isCollapsed) {
            seoText.classList.remove("collapsed");
            toggleBtn.classList.add("expanded");
            toggleBtn.setAttribute("aria-expanded", "true");
            toggleLabel.textContent = "Thu gọn";
        } else {
            seoText.classList.add("collapsed");
            toggleBtn.classList.remove("expanded");
            toggleBtn.setAttribute("aria-expanded", "false");
            toggleLabel.textContent = "Xem thêm";
        }
    });
}

window.getSeoContentByKey = getSeoContentByKey;
window.getSeoCategoryContent = getSeoCategoryContent;
window.renderSeoContent = renderSeoContent;
window.resolveSeoKeyByCurrentPage = resolveSeoKeyByCurrentPage;
